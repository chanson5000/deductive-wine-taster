package com.wineguesser.deductive.model;

public class WhiteVarietyDescriptors extends VarietyDescriptors {

    private int colorStraw;
    private int colorYellow;
    private int colorGold;

    private int phenolicBitterYes;
    private int phenolicBitterNo;

    private int fruitCitrus;
    private int fruitApplePear;
    private int fruitStonePit;
    private int fruitTropical;
    private int fruitMelon;

    public WhiteVarietyDescriptors() {

    }

    public int getColorStraw() {
        return colorStraw;
    }

    public void setColorStraw(int colorStraw) {
        this.colorStraw = colorStraw;
    }

    public int getColorYellow() {
        return colorYellow;
    }

    public void setColorYellow(int colorYellow) {
        this.colorYellow = colorYellow;
    }

    public int getColorGold() {
        return colorGold;
    }

    public void setColorGold(int colorGold) {
        this.colorGold = colorGold;
    }

    public int getPhenolicBitterYes() {
        return phenolicBitterYes;
    }

    public void setPhenolicBitterYes(int phenolicBitterYes) {
        this.phenolicBitterYes = phenolicBitterYes;
    }

    public int getPhenolicBitterNo() {
        return phenolicBitterNo;
    }

    public void setPhenolicBitterNo(int phenolicBitterNo) {
        this.phenolicBitterNo = phenolicBitterNo;
    }

    public int getFruitCitrus() {
        return fruitCitrus;
    }

    public void setFruitCitrus(int fruitCitrus) {
        this.fruitCitrus = fruitCitrus;
    }

    public int getFruitApplePear() {
        return fruitApplePear;
    }

    public void setFruitApplePear(int fruitApplePear) {
        this.fruitApplePear = fruitApplePear;
    }

    public int getFruitStonePit() {
        return fruitStonePit;
    }

    public void setFruitStonePit(int fruitStonePit) {
        this.fruitStonePit = fruitStonePit;
    }

    public int getFruitTropical() {
        return fruitTropical;
    }

    public void setFruitTropical(int fruitTropical) {
        this.fruitTropical = fruitTropical;
    }

    public int getFruitMelon() {
        return fruitMelon;
    }

    public void setFruitMelon(int fruitMelon) {
        this.fruitMelon = fruitMelon;
    }
}
